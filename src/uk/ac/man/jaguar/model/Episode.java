package uk.ac.man.jaguar.model;

/**
 *
 * @author osornogf
 */
public class Episode {    
    public int type;
    public int amount;
    public Episode(int type, int amount)
    {
        this.type = type;
        this.amount = amount;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
    
    
}
